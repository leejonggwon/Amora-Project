package com.model;

public class coffeeDTO {
	
	private String menuNm;
	private int price;
	private String size;
	
	
	public coffeeDTO(String menuNm, int price, String size) {
		super();
		this.menuNm = menuNm;
		this.price = price;
		this.size = size;
	}
	
	
	
	public String getMenuNm() {
		return menuNm;
	}
	public int getPrice() {
		return price;
	}
	public String getSize() {
		return size;
	}
	
	
	
	
	
	


	
	
	
	
	
	
	
	
	
	
	
}
